#include "../../include/minishell.h"

int	ft_isspace(int ch)
{
	return ((ch == ' ') || (ch >= 9 && ch <= 13));
}

int	ft_iswordsep(int ch)
{
	return (ft_isspace(ch) || ch == '|' || ch == '&'
		|| ch == '<' || ch == '>' || ch == '(' || ch == ')');
}
